public class Loops {
    public static void main(String[] args) {
        /* For Loop */
        for (int j = 0; j <= 15; j++) {
            System.out.print(j);
        }
        /* While Loop */
        int i=0;
        while(i<=10){
            System.out.println(i);
            i++;
        }
        /* Do While */
        int k=0;
        do
        {
            System.out.print(k);
            k++;
        }while(k<=10);
    }
}
